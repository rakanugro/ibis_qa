			array('customer_name' => 'xsd:string','customer_number' => 'xsd:string','insert_date_start' => 'xsd:string','insert_date_end' => 'xsd:string'),
            array('return' => 'xsd:string'),
            'urn:portalipc2',
            'urn:portalipc2#pollServer');
			
